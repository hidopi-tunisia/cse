<h2 class="m-0 font-lg font-semibold text-red-800"> PROTECTION DES DONNEES A CARACTERE PERSONNEL </h2>
<h3 class="mt-6 font-semibold text-red-700">Informatique et Libertés</h3>
<p>Les traitements informatiques de données à caractère personnel issus de ce site sont conformes au Règlement
    Général sur la Protection des Données du 27 avril 2016, applicable depuis le 25 mai 2018 ainsi qu'à la loi
    Informatique et Libertés n° 78-17 du 6 janvier 1978, modifiée au 6 août 2004. De ce fait, vous disposez d'un
    droit d'accès, de modification, de rectification et de suppression sur les données collectées sur ce site vous
    concernant. Pour exercer ces droits, adressez vous par email à dpo@presenceassistance.com.</p>
<p>Le traitement des données personnelles est nécessaire à l'adhésion et l'exécution de son contrat et de ses
    garanties, à la gestion des relations commerciales et contractuelles, ou à l'exécution de dispositions légales,
    réglementaires ou administratives en vigueur.</p>
<p><span class="puce">•&nbsp;</span>Les données collectées et traitées sont conservées pour la durée nécessaire à
    l'exécution du contrat ou de l'obligation légale. Ces données sont ensuite archivées conformément aux durées
    prévues par les dispositions relatives à la prescription.</p>
<p><span class="puce">•&nbsp;</span>Les destinataires des données le concernant sont, dans la limite de leurs
    attributions, les services de l'Assureur en charge de la passation, gestion et exécution du Contrat d'assurance
    et des garanties, ses délégataires, mandataires, partenaires, sous-traitants, réassureurs dans le cadre de
    l'exercice de leurs missions.</p>
<p>Elles peuvent également être transmises s'il y a lieu aux organismes professionnels ainsi qu'à toutes personnes
    intervenant au contrat tels qu'avocats, experts, auxiliaires de justice et officiers ministériels, curateurs,
    tuteurs, enquêteurs.</p>
<p>Des informations le concernant peuvent également être transmises au Souscripteur, ainsi qu'à toutes personnes
    habilitées au titre de Tiers Autorisés (juridictions, arbitres, médiateurs, ministères concernés, autorités de
    tutelle et de contrôle et tous organismes publics habilités à les recevoir ainsi qu'aux services en charge du
    contrôle tels les commissaires aux comptes, auditeurs ainsi que services en charge du contrôle interne).</p>
<p><span class="puce">•&nbsp;</span>En sa qualité d'organisme financier, l'Assureur est soumis aux obligations
    légales issues principalement du code monétaire et financier en matière de lutte contre le blanchiment des
    capitaux et contre le financement du terrorisme et, qu'à ce titre, il met en œuvre un traitement de surveillance
    des contrats pouvant aboutir à la rédaction d'une déclaration de soupçon ou à une mesure de gel des avoirs.</p>
<p><span class="puce">•&nbsp;</span>Ses données personnelles pourront également être utilisées dans le cadre d'un
    traitement de lutte contre la fraude à l'assurance pouvant conduire, le cas échéant, à une inscription sur une
    liste de personnes présentant un risque de fraude.</p>
<p>Les données et les documents concernant l'Assuré sont conservés pour une durée de cinq (5) ans à compter de la
    clôture du contrat ou de la cessation de la relation.</p>
<h3 class="mt-6 font-semibold text-red-700">Droit d'opposition au démarchage téléphonique</h3>
<p>Vous êtes un particulier et ne souhaitez pas faire l'objet de prospection commerciale par téléphone, vous pouvez
    gratuitement vous inscrire sur une liste d'opposition au démarchage téléphonique.</p>
<p>Notez toutefois qu'à ce jour, PRESENCE Assistance Tourisme ne pratique pas de démarchage téléphonique.</p>
